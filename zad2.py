# Largest number from the list
largest_number = max(7, 5, 6, 3, 8, 2)
print(f"Largest number: {largest_number}")

# Smallest number from the list
smallest_number = min(4, 7, 2, 3, 9, 8)
print(f"Smallest number: {smallest_number}")

# Length of the phrase "computer science"
phrase_length = len("computer science")
print(f"Length of the phrase: {phrase_length}")

# Letter read from the keyboard
letter = input("Enter a letter: ")
print(f"Letter read from the keyboard: {letter}")

# Convert the string "20303" to a number
string_to_number = int("20303")
print(f"Number representing the string '20303': {string_to_number}")

# Convert decimal 304 to binary
binary_representation = bin(304)
print(f"Binary string representing decimal number 304: {binary_representation}")

# Convert decimal 304 to hexadecimal
hexadecimal_representation = hex(304)
print(f"Hexadecimal string representing decimal number 304: {hexadecimal_representation}")

# Get the Unicode code of the € sign
unicode_of_euro = ord('€')
print(f"Integer representing the Unicode code of the € sign: {unicode_of_euro}")

# Absolute value of -17
absolute_value = abs(-17)
print(f"Absolute value of -17: {absolute_value}")
