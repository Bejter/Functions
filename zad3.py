#1
import math

# Square root of 7
sqrt_7 = math.sqrt(7)
print(f"Square root of 7: {sqrt_7}")

# Natural logarithm of 5
log_5 = math.log(5)
print(f"Natural logarithm of 5: {log_5}")

# Sine of 90 degrees (convert 90 degrees to radians first)
sin_90 = math.sin(math.radians(90))
print(f"Sine of 90 degrees: {sin_90}")


